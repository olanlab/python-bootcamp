import math

print(math.ceil(0.44))
print(math.floor(0.44))

# ABSOLUTE
print(math.fabs(-9))
print(math.fabs(9))

# FACTORIAL
print(math.factorial(3)) 

print(math.exp(2))
print(math.log(2))
print(math.log(2, 2))
print(math.pow(2, 2))
print(math.sqrt(4))

# Sin, Cos, Tan
print(math.sin(30))
print(math.cos(30))
print(math.tan(30))

print(math.asin(0.55))
print(math.acos(0.55))
print(math.atan(0.55))

print(math.sinh(0.55))
print(math.cosh(0.55))
print(math.tanh(0.55))