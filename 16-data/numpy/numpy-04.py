import numpy as np

# Create an array
arr = np.array([1, 2, 3, 4, 5])

# Mean of the array
mean_val = np.mean(arr)
print("Mean:", mean_val)

# Standard deviation of the array
std_dev = np.std(arr)
print("Standard Deviation:", std_dev)

# Sum of the array
sum_val = np.sum(arr)
print("Sum:", sum_val)

# Maximum value in the array
max_val = np.max(arr)
print("Maximum:", max_val)

# Minimum value in the array
min_val = np.min(arr)
print("Minimum:", min_val)
