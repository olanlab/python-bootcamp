# Assigning Values to Variables 
point = 100                         # integer
celsius = 36.5                      # floating
name = "Olan Samritjiarapon"        # string

print(point)
print(celsius)
print(name)

# Multiple Assignment
x = y = z = 1

print(x)
print(y)
print(z)

a, b, c = 100, 36.5, "Olan Samritjiarapon"

print(a)
print(b)
print(c)
