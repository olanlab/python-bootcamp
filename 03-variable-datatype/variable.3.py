# String
firstname = "Olan"
lastname = "Samritjiarapon"
print(firstname)
print(lastname)

# Concatenation
fullname = firstname + " " + lastname
print(fullname)

# Slice
print(fullname[0])

# Range Slice
print(fullname[1:3])

# Repetition
print(fullname*2)

# in
print("O" in fullname)
# not in 
print("O" not in fullname)

# Special Characters
print("Olan\tSamritjiarapon")
print("Olan\nSamritjiarapon")

