# Comparison Operators | Number
c = 10
d = 20

print("c = " + str(c), "d = " + str(d))
print("c == d", c == d)
print("c != d", c != d )
print("c > d", c > d )
print("c < d", c < d )
print("c >= d", c >= d )
print("c <= d", c <= d )

